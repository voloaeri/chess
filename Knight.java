

































































































































































































































































































































class Knight
  extends ChessPiece
{
  public Knight(ChessPlayer paramChessPlayer, ChessGame paramChessGame, ChessPosition paramChessPosition)
  {
    super(paramChessPlayer, paramChessGame, paramChessPosition);
    if (paramChessPlayer == paramChessGame.getPlayer1()) {
      mark = '♘';
    } else {
      mark = '♞';
    }
  }
  

  public void moveTo(ChessPosition paramChessPosition)
    throws IllegalMove
  {
    if ((Math.abs(paramChessPosition.getX() - getPosition().getX()) == 2) && (Math.abs(paramChessPosition.getY() - getPosition().getY()) == 1))
    {

      super.moveTo(paramChessPosition);
    } else if ((Math.abs(paramChessPosition.getX() - getPosition().getX()) == 1) && (Math.abs(paramChessPosition.getY() - getPosition().getY()) == 2))
    {

      super.moveTo(paramChessPosition);
    }
    else {
      throw new IllegalMove(this, getPosition(), paramChessPosition);
    }
  }
}
