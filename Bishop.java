
































































































































































































































































































class Bishop
  extends ChessPiece
{
  public Bishop(ChessPlayer paramChessPlayer, ChessGame paramChessGame, ChessPosition paramChessPosition)
  {
    super(paramChessPlayer, paramChessGame, paramChessPosition);
    if (paramChessPlayer == paramChessGame.getPlayer1()) {
      mark = '♗';
    } else {
      mark = '♝';
    }
  }
  

  public void moveTo(ChessPosition paramChessPosition)
    throws IllegalMove
  {
    if (paramChessPosition.getY() == getPosition().getY()) {
      throw new IllegalMove(this, getPosition(), paramChessPosition);
    }
    

    double d = Math.abs(paramChessPosition.getX() - getPosition().getX()) / Math.abs(paramChessPosition.getY() - getPosition().getY());
    


    if (d == 1.0D) {
      super.moveTo(paramChessPosition);
    }
    else
    {
      throw new IllegalMove(this, getPosition(), paramChessPosition);
    }
  }
}
